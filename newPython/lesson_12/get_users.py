


def list():
    users = "Lilit", "Aren", "Janna", "Samvel", "Gohar", "Armen", "Luiza"
    print(list(users))

def tuple():
    users = "Lilit", "Aren", "Janna", "Samvel", "Gohar", "Armen", "Luiza"

    print(tuple(users))

def set():
    users = "Lilit", "Aren", "Janna", "Samvel", "Gohar", "Armen", "Luiza"
    print(set(users))

